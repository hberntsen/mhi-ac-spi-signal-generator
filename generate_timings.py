pin_map = {
        '!':'CS',
        '"':'MISO',
        '#':'CLK',
        '$':'MOSI'
        }

def get_events():
    for line in raw_events_str.strip().split('\n'):
        parts = line.split()
        t = int(parts[0][1:])
        for sig in parts[1:]:
            val = int(sig[0])
            pin = pin_map[sig[1]]
            yield (t, pin, val)

events = list(get_events())
events.sort()


TS = 10  # ns per unit

RMT_NS_PER_TICK=25

start = ((20340 - 620) * (RMT_NS_PER_TICK / 10) ) 

# * 10 ns for source units, / 25 because of 40MHz rmt resolution clock
mosi_events = [(t - start, v) for t,s,v in events if s == 'MOSI']
clk_events = [(t - start, v) for t,s,v in events if s == 'CLK']

mosi_events[0] = (0, mosi_events[0][1])
clk_events[0] = (0, clk_events[0][1])

def timestamp_to_duration(events_in):
    for (t0,v0),(t1,v1) in zip(events_in, events_in[1:]):
        yield (t1 - t0, v0)
    yield (1525, events_in[-1][1])


# extend when we reach maximum
def to_rmt_array(events_in):
    items_yielded = 0
    last_state = events_in[-1][1]
    # add end marker
    for t,v in events_in + [(0, last_state)]:
        # sourse resolution is 10 ns per
        new_t = round(t / (RMT_NS_PER_TICK / 10))
        while new_t >= 0:
            if new_t > 0x7fff:
                new_t -= 0x7fff
                items_yielded += 1
                yield (0x7fff, v)
            else:
                items_yielded += 1
                yield (new_t, v)
                break

    if items_yielded % 2 == 1:
        yield (0, last_state)

def make_c(rmt_array):
    for ((t0,v0), (t1,v1)) in zip(rmt_array[::2], rmt_array[1::2]):
        print(f"{{.level0={v0},.duration0={t0},.level1={v1},.duration1={t1}}},")

print("static const rmt_symbol_word_t clk_symbols[] = {")
make_c(list(to_rmt_array(list(timestamp_to_duration(clk_events)))))
print("};")
print("static const rmt_symbol_word_t mosi_symbols[] = {")
make_c(list(to_rmt_array(list(timestamp_to_duration(mosi_events)))))
print("};")

